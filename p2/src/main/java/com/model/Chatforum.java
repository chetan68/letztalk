package com.model;

public class Chatforum {
	
	private int Chatforumid ;
	
	private int Userid;
	
	private String Chatforumname;

	public int getChatforumid() {
		return Chatforumid;
	}

	public void setChatforumid(int chatforumid) {
		Chatforumid = chatforumid;
	}

	public int getUserid() {
		return Userid;
	}

	public void setUserid(int userid) {
		Userid = userid;
	}

	public String getChatforumname() {
		return Chatforumname;
	}

	public void setChatforumname(String chatforumname) {
		Chatforumname = chatforumname;
	}
	
	

}
